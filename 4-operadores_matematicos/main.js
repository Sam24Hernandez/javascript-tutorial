/* Operadores aritméticos */
var num1 = 10;
var num2 = 50;
console.log('División de ' + num2 + ' entre ' + num1 + ' es igual a:');
console.log(num2 / num1);

/* 
    Operadores de asignación:
    Los operadores de asignación son operadores que asignan un valor a una variable. Ya usamos el más básico, =, 
    muchas veces — simplemente asigna a la variable de la izquierda, el valor de la derecha:
*/
var x = 3; // x contiene el valor 3
var y = 4; // y contiene el valor 4
x = y; // x ahora contiene el mismo valor de y...4

// Pero hay algunos tipos más complejos, checa la nota 4-operadores, para verlos:
var x = 3; // x contiene el valor 3
var y = 4; // y contiene el valor 4
x *= y; // x ahora contiene el valor 12

